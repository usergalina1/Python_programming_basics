# find min of numbers
# min()-returns minimum value
print(min(10, 50, 1, 15))  # 1
print(min(10.5, 50.1, 1.8, 15.4)) # 1.8

# find max of numbers
# min()-returns maximum value
print(max(10, 50, 1, 15))  # 50
print(max(10.5, 50.1, 1.8, 15.4)) # 50.1